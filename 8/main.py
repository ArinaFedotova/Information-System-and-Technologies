def asking(m, test_name, q, ind):
    global points
    global a_count, b_count

    if ind >= len(q):
        bot.send_message(m.chat.id, f'–¢–µ—Å—Ç –∑–∞–≤–µ—Ä—à–µ–Ω! üéâ –ò–∑ –≤—Å–µ—Ö {points} –æ—Ç–≤–µ—Ç–æ–≤ –≤—ã –≤—ã–±–∏—Ä–∞–ª–∏ –≤–∞—Ä–∏–∞–Ω—Ç –∞ {a_count} —Ä–∞–∑, '
                                    f'–∞ –≤–∞—Ä–∏–∞–Ω—Ç –± {b_count} —Ä–∞–∑! –û–±—Ä–∞–±–∞—Ç—ã–≤–∞—é —Ä–µ–∑—É–ª—å—Ç–∞—Ç... ü§î')
        process_results(m, test_name)

        keyboard = types.ReplyKeyboardMarkup(resize_keyboard=True)
        keyboard.add(types.KeyboardButton('–í–µ—Ä–Ω—É—Ç—å—Å—è –Ω–∞–∑–∞–¥ üîô'))
        bot.send_message(m.chat.id, '–í—ã –º–æ–∂–µ—Ç–µ –≤–µ—Ä–Ω—É—Ç—å—Å—è –Ω–∞–∑–∞–¥ –≤ –º–µ–Ω—é üîÑ.', reply_markup=keyboard)
        return

    question, options = q[ind]

    keyboard = types.ReplyKeyboardMarkup(resize_keyboard=True)
    keyboard.add(types.KeyboardButton('–∞'), types.KeyboardButton('–±'))

    msg_text = f"{question}\n\n–∞: {options[0]}\n–±: {options[1]}"

    msg = bot.send_message(m.chat.id, msg_text, reply_markup=keyboard)
    bot.register_next_step_handler(msg, process_answer, test_name, q, ind)


def process_answer(m, test_name, questions, index):
    global points
    global a_count, b_count
    points += 1
    if m.text == '–∞':
        a_count += 1
    elif m.text == '–±':
        b_count += 1

    asking(m, test_name, questions, index + 1)


